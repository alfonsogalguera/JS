let a=1, b=2;
let z=a+b;

//incremento
//pre-incremento(el operador ++ antes de la variable)
z= ++a;
console.log(z);
console.log(a);
//post-incremento(el operador ++ despues de la variable)
z=b++;
console.log(b);
console.log(z);
//decremento 
//predecremento

z=--a;
console.log(z);
console.log(a);

//post-decremento
z=b--;
console.log(z);
console.log(b);